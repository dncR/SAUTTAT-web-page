                                <p>
                                    The aim of the workshop is to introduce participants to explainable artificial intelligence (XAI) methods that can be used to build
                                    predictive models and extract knowledge from predictive models. The workshop will combine discussion of the theoretical basis together
                                    with examples with code for your own execution. We will use real-world data for a   mortality prediction problem for covid or
                                    classification problem for heart disease.
                                </p>
                                <p>
                                    The discussed methods are available in many programming languages and various libraries, but the workshop will be based on examples
                                    in R using the DALEX library. The scope of the workshop coincides with that of the book Explanatory Model Analysis
                                    <a href="https://ema.drwhy.ai/">https://ema.drwhy.ai/</a>.
                                </p>
                                <p>
                                    The first part of the workshop is dedicated to exploratory data analysis tools and preparing for modelling. The second part of the
                                    workshop is focused on tools for developing predictive models. For the purposes of the example, we will discuss decision trees,
                                    random forests and techniques for automatic tuning of random forests. The third part will focus on local model explanation techniques.
                                    We will discuss SHAP (Shapley values), break-down and LIME, the most popular methods for local exploration of models. The fourth part
                                    will be devoted to global model explanation techniques. We will discuss the permutation importance technique for variables and the
                                    Partial Dependence technique. The workshop will be based on material from <a href="https://github.com/BetaAndBit/RML">https://github.com/BetaAndBit/RML</a>
                                </p>

                                <h3>Why?</h3>
                                <p>
                                    Complex machine learning models are frequently used in predictive modeling. There are a lot of examples for random forest-like or
                                    boosting-like models in medicine, finance, agriculture, etc. But who trusts in black boxes? In this workshop we will show why and
                                    how one would analyse the structure of the black-box model. This will be a hands-on workshop. In each part there will be a short
                                    lecture and then time for practice and discussion. Using the example of analysing a specific dataset, we will show the basics of
                                    modelling with tree models. We will then show how to evaluate and analyse such models using XAI techniques. From the packages, we
                                    will learn about randomForest, party, mlr3, DALEX, modelStudio and arenar.

                                <p>
                                    Large scale datasets generated by different omics technologies present unique challenges in terms of normalization and integration.
                                    This course focuses on expanding biostatistical and bioinformatics methods for such challenges. We will be focusing on the RUV
                                    normalization methods, which have shown great promise in dealing with the challenges presented by large scale datasets from TCGA.
                                    RUV-PRPS which is a novel strategy (Molania et al, 2023, Nat. Biotech,
                                    <a href="https://www.nature.com/articles/s41587-022-01440-w#code-availability">https://www.nature.com/articles/s41587-022-01440-w#code-availability</a>)
                                    uses pseudo-replicates of pseudo-samples (PRPS) to normalize RNA-seq data in situations when technical replicate is not available.
                                    In this course we will be presenting the new RUV-PRPS package we have been developing, which is a user-friendly R package that enable
                                    researchers to run RUV-PRPS method and to visualize diagnostic plots before and after normalization to assess the quality and consistency
                                    of their data.
                                </p>

